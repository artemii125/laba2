print "Введите кол-во полных банок и эквивалент обмена: "
cups = gets.split.map(&:to_i) # cups[0] — начальные банки, cups[1] — курс обмена

drunk = cups[0] # изначально выпито
i = 0 # счётчик шагов
full = 0 # количество полных банок после обмена

# пока можно менять пустые банки на полные
while cups[0] >= cups[1]
  i += 1 # шаг: опустошили
  full = cups[0] / cups[1] # обмен: получили полные банки
  i += 1 # шаг: обмен завершён
  empty = cups[0] % cups[1] # остаток пустых банок
  drunk += full # прибавляем к общему количеству выпитого
  cups[0] = full + empty # обновляем текущие банки
end

i += 1 # последний шаг: выпиваем остатки
puts "Выпито банок и кол-во итераций: #{drunk} #{i}"
